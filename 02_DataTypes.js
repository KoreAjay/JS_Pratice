"use strict"; //treat all js code as newer version

// Data types
// 1. Primitive data types: string, number, boolean, null, undefined, symbol
// 2. Non-primitive data types: object, array, function

// String
let name = "John Doe";
console.log(name);

// Number
let age = 30;
console.log(age);

// Boolean
let isStudent = true;
console.log(isStudent);

// Null
let emptyValue = null;
console.log(emptyValue);

// Undefined
let undefinedValue;
console.log(undefinedValue);

// Symbol
let uniqueId = Symbol("id");
console.log(uniqueId);

// Object
let person = {
    name: "John Doe",
    age: 30,
    isStudent: true
};
console.log(person);

// Array
let numbers = [1, 2, 3, 4, 5];
console.log(numbers);

// Function
function greet() {
    console.log("Hello!");
}
greet();
